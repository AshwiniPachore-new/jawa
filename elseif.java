public class elseif {
    public static void main(String[] args) {
        int number = 10;

        if (number >= 5) {
            System.out.println("Number is small");
        } else if (number == 3) {
            System.out.println("Number is too small");
        } else {
            System.out.println("Number is invalid");
        }
    }
}
